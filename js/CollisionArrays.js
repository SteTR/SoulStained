    detectCollision(section, y, x) {
        // hero x and y, the canvas relative coordinates 720x720, 720 /12 = 60
        const y_translated = Math.floor(y / 60);
        const x_translated = Math.floor(x / 60);
        const sectionString = "section" + section.x + "_" + section.y;
        let currentSection = this[sectionString];
        if (currentSection[y_translated][x_translated] === 0 || currentSection[y_translated][x_translated] === undefined) {
            return false;
        }
        else {
            return true;
        }
    }
